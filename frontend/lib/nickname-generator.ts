export function generateNickname(): string {
  const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const animal = animals[Math.floor(Math.random() * animals.length)];
  
  return `${adjective}${animal}`;
}

export function generateNicknameWithNoun(): string {
  const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const noun = nouns[Math.floor(Math.random() * nouns.length)];
  
  return `${adjective}${noun}`;
}

export function generateNicknameWithNumber(): string {
  const animal = animals[Math.floor(Math.random() * animals.length)];
  const number = Math.floor(Math.random() * 9999);
  
  return `${animal}${number}`;
}

export function generateUniqueNickname(): string {
  const base = generateNickname();
  const suffix = Math.floor(Math.random() * 999);
  
  return suffix > 0 ? `${base}${suffix}` : base;
}